    private void jLabel5MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel5MouseClicked
        String gmailll =gmail12.getText(); 
      
        String User=username.getText();

        Border b = BorderFactory.createLineBorder(Color.green, 2);

        try {
            Connection con = ConnectionProvider.getcon();
            Statement st1=con.createStatement();
            Statement st2=con.createStatement();
            String sql1=" Select * from  details_verify where gmail_ID='"+gmailll+"'";
            String sql2=" Select * from  details_verify where Username='"+User+"'";
            ResultSet rs1=st1.executeQuery(sql1);
            ResultSet rs2=st2.executeQuery(sql2);
            if(rs2.next())
            {
                if(rs1.next()){
                    gmail=gmail12.getText(); 
                    gmail12.setText("");
                    username.setText("");
                    gmail12.setEditable(false);
                    username.setEditable(false);
                    pass1.setEditable(true);
                    pass2.setEditable(true);
                    pass1.setBorder(b);
                    pass2.setBorder(b);
                    jLabel5.setEnabled(false);
                }else{
                    JOptionPane.showMessageDialog(null,"Invalid Gmail and Username");
                    gmail12.setText("");
                    username.setText("");}
            }else{
                JOptionPane.showMessageDialog(null,"Invalid Username ");
            }

        } catch (HeadlessException | SQLException e) {

            JOptionPane.showMessageDialog(null,e);
        }

    }//GEN-LAST:event_jLabel5MouseClicked

    
    
    
